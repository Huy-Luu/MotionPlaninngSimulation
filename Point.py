
class Point(object):

    def __init__(self, x, y):
        self.x = x
        self.y = y

    def getX():
        return self.x

    def getY():
        return self.y

class OriginalPoint(object):
    def __init__(self, lat, lon):
        self.lat = lat
        self.lon = lon

    def getLat(self):
        return self.lat

    def getLon(self):
        return self.lon